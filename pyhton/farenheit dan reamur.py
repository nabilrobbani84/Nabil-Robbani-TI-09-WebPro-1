#program konversi suhu dari celcius ke farenheit dan reamur
#input
celcius = input("Masukkan suhu celcius yang ingin diubah = ")

#rumus
farenheit = 9/5 * int(celcius) + 32
reamur = 4/5 * int(celcius)

#output
print("Hasil konversi dari celcius ke farenheit adalah ", farenheit)
print("Hasil konversi dari celcius ke reamur adalah = ", reamur)